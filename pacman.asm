clear_screen:
    push es
    push ax
    push di

    mov ax, 0xb800
    mov es, ax
    xor di, di

nextloc:
    mov word [es:di], 0x0720  ; space with attribute 7 (gray)
    add di, 2
    cmp di, 4000              ; 80 cols * 25 rows * 2 bytes per char = 4000 bytes
    jne nextloc

    pop di
    pop ax
    pop es
    ret

