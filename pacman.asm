outer_loop:
    cmp si, 25
    jg done

    xor di, di          ; col = 0

inner_loop:
    cmp di, 80
    jge next_row

    ; offset = row * 80 + col
    mov ax, si
    mov bx, 80
    mul bx              ; ax = row * 80
    add ax, di          ; ax = offset

    lea bx, [map]
    add bx, ax          ; bx = address of map[row][col]

    mov al, [bx]
    cmp al, 2
	je print_Blue 
	cmp al , 3 
	je print_o
	cmp al , 4 
	je print_yellow 
	cmp al , 5 
	je print_green
	jmp skip_print	
	
	
	

	
print_o:
    push di             ; Save column counter
    mov ax, si
    mov dx, 80
    mul dx              ; ax = row * 80
    add ax, di          ; ax = row * 80 + col
    shl ax, 1           ; multiply by 2 (char+attr)
    mov di, ax          ; DI now holds video memory offset
    mov ax, 0x0C00| 0xDB  ; attr 0x19, char 0xDB
    mov [es:di], ax
    pop di 
    jmp skip_print	
